class Concat(nn.Module):
    """Concatenation module to combine outputs along a given dimension."""
    def __init__(self, dim, *modules):
        super(Concat, self).__init__()
        self.dim = dim
        # Add each module as a submodule of this module
        for idx, module in enumerate(modules):
            self.add_module(str(idx), module)
    def forward(self, x):
        # Forward input through all submodules and concatenate results
        outputs = [module(x) for module in self._modules.values()]
        if not outputs:
            return x
        # If spatial dimensions differ, crop to the smallest size before concatenation
        min_h = min(out.shape[2] for out in outputs)
        min_w = min(out.shape[3] for out in outputs)
        outputs = [out[:, :, :min_h, :min_w] for out in outputs]
        return torch.cat(outputs, dim=self.dim)

def act(act_fun='LeakyReLU'):
    """Return an activation function module given a name or module."""
    if isinstance(act_fun, str):
        act_fun = act_fun.lower()
        if act_fun == 'leakyrelu':
            return nn.LeakyReLU(0.2, inplace=True)
        elif act_fun == 'relu':
            return nn.ReLU(inplace=True)
        elif act_fun == 'elu':
            return nn.ELU(inplace=True)
        elif act_fun == 'swish':
            class Swish(nn.Module):
                def __init__(self):
                    super(Swish, self).__init__()
                    self.sig = nn.Sigmoid()
                def forward(self, x):
                    return x * self.sig(x)
            return Swish()
        elif act_fun == 'none':
            return nn.Identity()
        else:
            raise ValueError(f"Unknown activation function: {act_fun}")
    # If act_fun is already an nn.Module (or a class), return an instance or the module itself
    if isinstance(act_fun, type):
        return act_fun()
    return act_fun

def bn(num_features):
    """Return a 2D batch normalization layer."""
    return nn.BatchNorm2d(num_features)

def conv(in_f, out_f, kernel_size, stride=1, bias=True, pad='zero', downsample_mode='stride'):
    """
    Create a convolutional layer (with optional padding and downsampling).
    If pad='reflection', uses ReflectionPad2d. Otherwise uses zero-padding via Conv2d's padding.
    If downsample_mode is 'avg' or 'max' and stride > 1, applies pooling for downsampling.
    """
    # Padding setup
    padder = None
    pad_val = (kernel_size - 1) // 2  # half the kernel for symmetric padding
    if pad == 'reflection':
        if pad_val != 0:
            padder = nn.ReflectionPad2d(pad_val)
            pad_val = 0  # Conv will not handle padding if we explicitly pad
    # Downsampler setup (for stride > 1)
    downsampler = None
    if stride != 1 and downsample_mode != 'stride':
        if downsample_mode == 'avg':
            downsampler = nn.AvgPool2d(stride, stride)
        elif downsample_mode == 'max':
            downsampler = nn.MaxPool2d(stride, stride)
        else:
            raise ValueError(f"Unknown downsample_mode: {downsample_mode}")
        stride = 1  # use pooling instead of stride in conv
    # Convolutional layer
    conv_layer = nn.Conv2d(in_f, out_f, kernel_size, stride, padding=pad_val, bias=bias)
    # If we need padding or downsampling, wrap them in a Sequential
    if padder or downsampler:
        layers = []
        if padder:
            layers.append(padder)
        layers.append(conv_layer)
        if downsampler:
            layers.append(downsampler)
        return nn.Sequential(*layers)
    else:
        return conv_layer

def skip(num_input_channels=3, num_output_channels=3,
         num_channels_down=(128, 128, 128, 128, 128),
         num_channels_up=(128, 128, 128, 128, 128),
         num_channels_skip=(4, 4, 4, 4, 4),
         filter_size_down=3, filter_size_up=3, filter_skip_size=1,
         need_sigmoid=True, need_bias=True, pad='reflection',
         upsample_mode='nearest', downsample_mode='stride', act_fun='LeakyReLU',
         need1x1_up=True):
    """
    Assemble an encoder-decoder network with skip connections (Deep Image Prior 'skip' architecture).
    """
    # Ensure parameters are list/tuple
    n_scales = len(num_channels_down)
    assert n_scales == len(num_channels_up) == len(num_channels_skip), "Channel lists must have the same length"
    if not isinstance(filter_size_down, (list, tuple)):
        filter_size_down = [filter_size_down] * n_scales
    if not isinstance(filter_size_up, (list, tuple)):
        filter_size_up = [filter_size_up] * n_scales
    if not isinstance(upsample_mode, (list, tuple)):
        upsample_mode = [upsample_mode] * n_scales
    if not isinstance(downsample_mode, (list, tuple)):
        downsample_mode = [downsample_mode] * n_scales

    # Main model is a Sequential container
    model = nn.Sequential()
    # We'll build the U-Net like structure by iterating over scales
    input_depth = num_input_channels
    # Pointers for building nested module structure
    model_current = model  # current working Sequential to append to
    for scale in range(n_scales):
        # Create new sequential modules for deeper (downsampling path) and skip connection
        deeper_seq = nn.Sequential()
        skip_seq = nn.Sequential()
        # If skip connection channels > 0, add concatenation of skip and deeper
        if num_channels_skip[scale] != 0:
            model_current.add_module(f"concat_{scale}", Concat(dim=1, *[skip_seq, deeper_seq]))
        else:
            # No skip connection at this scale, add deeper_seq directly
            model_current.add_module(f"skip_none_{scale}", deeper_seq)
        # After combining skip and deeper, add batch norm
        out_channels = num_channels_skip[scale] + (num_channels_up[scale+1] if scale < n_scales-1 else num_channels_down[scale])
        model_current.add_module(f"bn_combo_{scale}", bn(out_channels))
        # Build skip connection branch (one conv + BN + activation if channels > 0)
        if num_channels_skip[scale] != 0:
            skip_seq.add_module(f"conv_skip_{scale}", conv(input_depth, num_channels_skip[scale],
                                                           filter_skip_size, stride=1,
                                                           bias=need_bias, pad=pad))
            skip_seq.add_module(f"bn_skip_{scale}", bn(num_channels_skip[scale]))
            skip_seq.add_module(f"act_skip_{scale}", act(act_fun))
        # Build deeper (encoder) branch with two convolutions and activations
        deeper_seq.add_module(f"conv_down_{scale}_0", conv(input_depth, num_channels_down[scale],
                                                           filter_size_down[scale], stride=2,
                                                           bias=need_bias, pad=pad, downsample_mode=downsample_mode[scale]))
        deeper_seq.add_module(f"bn_down_{scale}_0", bn(num_channels_down[scale]))
        deeper_seq.add_module(f"act_down_{scale}_0", act(act_fun))
        deeper_seq.add_module(f"conv_down_{scale}_1", conv(num_channels_down[scale], num_channels_down[scale],
                                                           filter_size_down[scale], stride=1,
                                                           bias=need_bias, pad=pad))
        deeper_seq.add_module(f"bn_down_{scale}_1", bn(num_channels_down[scale]))
        deeper_seq.add_module(f"act_down_{scale}_1", act(act_fun))
        # If not at the last scale, prepare for next nested block
        if scale < n_scales - 1:
            # Create a new deeper_main sequential to go down another level
            deeper_main = nn.Sequential()
            deeper_seq.add_module(f"deeper_{scale}", deeper_main)
            # After descending deeper, add upsampling to bring features back up
            deeper_seq.add_module(f"upsample_{scale}", nn.Upsample(scale_factor=2, mode=upsample_mode[scale]))
            # Build the upsampling (decoder) branch: one or two conv layers to combine skip and deeper features
            model_current.add_module(f"conv_up_{scale}_0", conv(num_channels_skip[scale] + num_channels_up[scale+1],
                                                                num_channels_up[scale], filter_size_up[scale],
                                                                stride=1, bias=need_bias, pad=pad))
            model_current.add_module(f"bn_up_{scale}_0", bn(num_channels_up[scale]))
            model_current.add_module(f"act_up_{scale}_0", act(act_fun))
            if need1x1_up:
                model_current.add_module(f"conv_up_{scale}_1", conv(num_channels_up[scale], num_channels_up[scale],
                                                                    kernel_size=1, stride=1, bias=need_bias, pad=pad))
                model_current.add_module(f"bn_up_{scale}_1", bn(num_channels_up[scale]))
                model_current.add_module(f"act_up_{scale}_1", act(act_fun))
            # Move the pointer to the next deeper level for the next iteration
            input_depth = num_channels_down[scale]  # update input depth for next scale
            model_current = deeper_main  # go one level deeper
        else:
            # Last scale: at the bottom of U-Net, just upsample back up after convs
            deeper_seq.add_module(f"upsample_{scale}", nn.Upsample(scale_factor=2, mode=upsample_mode[scale]))
            # Combine skip and deepest features on the way up
            model_current.add_module(f"conv_up_{scale}_0", conv(num_channels_skip[scale] + num_channels_down[scale],
                                                                num_channels_up[scale], filter_size_up[scale],
                                                                stride=1, bias=need_bias, pad=pad))
            model_current.add_module(f"bn_up_{scale}_0", bn(num_channels_up[scale]))
            model_current.add_module(f"act_up_{scale}_0", act(act_fun))
            if need1x1_up:
                model_current.add_module(f"conv_up_{scale}_1", conv(num_channels_up[scale], num_channels_up[scale],
                                                                    kernel_size=1, stride=1, bias=need_bias, pad=pad))
                model_current.add_module(f"bn_up_{scale}_1", bn(num_channels_up[scale]))
                model_current.add_module(f"act_up_{scale}_1", act(act_fun))
            # No deeper level to descend to, so no pointer update needed

    # Final output layer: 1x1 convolution to get desired output channels
    model.add_module("final_conv", conv(num_channels_up[0], num_output_channels, kernel_size=1,
                                       stride=1, bias=need_bias, pad=pad))
    if need_sigmoid:
        model.add_module("output_sigmoid", nn.Sigmoid())
    return model

def get_net(input_depth, NET_TYPE="skip", pad='reflection', upsample_mode='nearest',
            n_channels=3, skip_n33d=128, skip_n33u=128, skip_n11=4, num_scales=5):
    """
    Create a network of a given type with the specified parameters.
    NET_TYPE 'skip' yields the DIP skip network (U-Net with skip connections).
    """
    if NET_TYPE == 'skip':
        # Define channel counts for each scale based on given parameters
        num_channels_down = [skip_n33d] * num_scales
        num_channels_up   = [skip_n33u] * num_scales
        num_channels_skip = [skip_n11]  * num_scales
        # Assemble the skip network
        net = skip(num_input_channels=input_depth, num_output_channels=n_channels,
                   num_channels_down=num_channels_down, num_channels_up=num_channels_up,
                   num_channels_skip=num_channels_skip, filter_size_down=3, filter_size_up=3,
                   filter_skip_size=1, need_sigmoid=True, need_bias=True, pad=pad,
                   upsample_mode=upsample_mode, downsample_mode='stride',
                   act_fun='LeakyReLU', need1x1_up=True)
    else:
        raise ValueError(f"Unsupported NET_TYPE: {NET_TYPE}")
    return net

def load_dip_model(weights_path, map_location='cpu'):
    """
    Load a Deep Image Prior model from a checkpoint, ensuring it's on CPU.
    The checkpoint is expected to contain a state dict of the network.
    """
    # Load the saved state dict (force onto CPU)
    state = torch.load(weights_path, map_location=map_location)
    # Some DIP checkpoints might save a dict with 'net' key or full model; handle accordingly
    if isinstance(state, dict) and 'net' in state:
        state = state['net']
    # Build the DIP skip network architecture (using default DIP parameters)
    net = get_net(input_depth=32, NET_TYPE='skip', pad='reflection',
                  upsample_mode='nearest', n_channels=3,
                  skip_n33d=128, skip_n33u=128, skip_n11=4, num_scales=5)
    # Load the weights into the network
    net.load_state_dict(state)
    net.eval()  # Set in evaluation mode by default
    return net

