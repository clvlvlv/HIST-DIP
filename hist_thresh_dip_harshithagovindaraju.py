def skip(num_input_channels=3, num_output_channels=3,
         num_channels_down=[128, 128, 128, 128, 128],
         num_channels_up=[128, 128, 128, 128, 128],
         num_channels_skip=[4, 4, 4, 4, 4],
         filter_size_down=3, filter_size_up=3, filter_skip_size=1,
         need_sigmoid=True, need_bias=True,
         pad='reflection', upsample_mode='nearest',
         downsample_mode='stride', act_fun='LeakyReLU',
         need1x1_up=True):

    assert len(num_channels_down) == len(num_channels_up) == len(num_channels_skip)
    n_scales = len(num_channels_down)

    def act(name='LeakyReLU'):
        if name == 'LeakyReLU':
            return nn.LeakyReLU(0.2, inplace=True)
        elif name == 'ReLU':
            return nn.ReLU(inplace=True)
        elif name == 'ELU':
            return nn.ELU(inplace=True)
        else:
            return nn.Identity()

    def bn(num_features):
        return nn.BatchNorm2d(num_features)

    def conv(in_f, out_f, kernel_size, stride=1, bias=True, pad='zero'):
        pad_val = (kernel_size - 1) // 2
        if pad == 'reflection':
            layers = [nn.ReflectionPad2d(pad_val)]
            pad_val = 0
        else:
            layers = []

        layers.append(nn.Conv2d(in_f, out_f, kernel_size, stride, padding=pad_val, bias=bias))
        return nn.Sequential(*layers)

    input_depth = num_input_channels
    model = nn.Sequential()
    model_tmp = model

    for i in range(n_scales):
        deeper = nn.Sequential()
        skip = nn.Sequential()
        model_tmp.add_module(f"skip_{i}", skip)
        model_tmp.add_module(f"deeper_{i}", deeper)

        if num_channels_skip[i] != 0:
            skip.add_module(f"conv_skip_{i}", conv(input_depth, num_channels_skip[i], filter_skip_size, bias=need_bias, pad=pad))
            skip.add_module(f"bn_skip_{i}", bn(num_channels_skip[i]))
            skip.add_module(f"act_skip_{i}", act(act_fun))

        deeper.add_module(f"conv_down_{i}_1", conv(input_depth, num_channels_down[i], filter_size_down, stride=2, bias=need_bias, pad=pad))
        deeper.add_module(f"bn_down_{i}_1", bn(num_channels_down[i]))
        deeper.add_module(f"act_down_{i}_1", act(act_fun))

        deeper.add_module(f"conv_down_{i}_2", conv(num_channels_down[i], num_channels_down[i], filter_size_down, bias=need_bias, pad=pad))
        deeper.add_module(f"bn_down_{i}_2", bn(num_channels_down[i]))
        deeper.add_module(f"act_down_{i}_2", act(act_fun))

        deeper_main = nn.Sequential()

        if i == n_scales - 1:
            k = num_channels_down[i]
        else:
            deeper.add_module(f"sub_deeper_{i}", deeper_main)
            k = num_channels_up[i + 1]

        deeper.add_module(f"upsample_{i}", nn.Upsample(scale_factor=2, mode=upsample_mode))

        model_tmp.add_module(f"conv_up_{i}_1", conv(num_channels_skip[i] + k, num_channels_up[i], filter_size_up, bias=need_bias, pad=pad))
        model_tmp.add_module(f"bn_up_{i}_1", bn(num_channels_up[i]))
        model_tmp.add_module(f"act_up_{i}_1", act(act_fun))

        if need1x1_up:
            model_tmp.add_module(f"conv_up_{i}_2", conv(num_channels_up[i], num_channels_up[i], 1, bias=need_bias, pad=pad))
            model_tmp.add_module(f"bn_up_{i}_2", bn(num_channels_up[i]))
            model_tmp.add_module(f"act_up_{i}_2", act(act_fun))

        input_depth = num_channels_down[i]
        model_tmp = deeper_main

    model.add_module("final_conv", conv(num_channels_up[0], num_output_channels, 1, bias=need_bias, pad=pad))
    if need_sigmoid:
        model.add_module("sigmoid", nn.Sigmoid())

    return model

