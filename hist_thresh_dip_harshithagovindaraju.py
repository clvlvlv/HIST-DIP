@app.route('/processImage', methods=['POST'])
def process_image():
    if 'image' not in request.files:
        return jsonify({'message': 'No image provided'}), 400

    image_file = request.files['image']
    original_filename = os.path.splitext(image_file.filename)[0]

    try:
        # 1) Load and transform image
        image = Image.open(image_file).convert("L")  # or "RGB" if you prefer color

        transform_pipeline = transforms.Compose([
            # Possibly do: transforms.Resize((1184, 896)),
            transforms.ToTensor()
        ])

        # shape => [1,1,H,W]
        img_tensor = transform_pipeline(image).unsqueeze(0)
        # DIP expects 32 input channels (based on your training), so repeat:
        # shape => [1,32,H,W]
        img_tensor = img_tensor.repeat(1, 32, 1, 1)

        # 2) Inference (no gradient)
        with torch.no_grad():
            output_tensor = net(img_tensor)

        # 3) Convert output to PIL
        # Suppose the DIP final layer is [1,3,H,W]
        output_np = output_tensor.squeeze(0).cpu().numpy()  # shape => [3,H,W]
        output_np = np.clip(output_np, 0, 1)
        output_np = (output_np * 255).astype(np.uint8)
        output_np = np.transpose(output_np, (1, 2, 0))  # => [H,W,3]

        out_img = Image.fromarray(output_np)

        # 4) Save locally
        output_filename = f"{original_filename}-output.png"
        image_path = os.path.join(PROCESSED_IMAGE_PATH, output_filename)
        out_img.save(image_path)

        return jsonify({
            'message': 'Image processed and saved successfully',
            'image_path': output_filename
        }), 200

    except Exception as e:
        return jsonify({
            'message': 'Error processing image',
            'error': str(e)
        }), 500

